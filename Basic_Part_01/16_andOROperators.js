// and OR operators

let firstName = "shehbaz";
let age = 18;

// AND && (check both condition)

if (firstName[0] === "s" && age === 18) {
  console.log("you are right person");
} else {
  console.log("you are wrong person");
}


//   ||  OR ()

if (firstName[0] === "s" || age === 16) {
  console.log("you are right person");
} else {
  console.log("you are wrong person");
}
